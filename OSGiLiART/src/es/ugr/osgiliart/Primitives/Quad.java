package es.ugr.osgiliart.Primitives;

// El cuadilátero.
public class Quad {
	
	private 
		
		int x1, y1, x2, y2, x3, y3, x4, y4; // Xn, Yn son las coordenadas de cada vértice.
	
	public
	
	double getX1()
	{
		return x1;
	}

	double getY1()
	{
		return y1;
	}
	
	double getX2()
	{
		return x2;
	}

	double getY2()
	{
		return y2;
	}
	
	
	double getX3()
	{
		return x3;
	}

	double getY3()
	{
		return y3;
	}
	
	double getX4()
	{
		return x4;
	}

	double getY4()
	{
		return y4;
	}
	
	
	
	void setX1( int x)
	{
		 x1 = x;
	}
	
	void setY1(int y)
	{
		y1 = y;
	}
	
	void setX2( int x)
	{
		 x2 = x;
	}
	
	void setY2(int y)
	{
		y2 = y;
	}
	
	void setX3( int x)
	{
		 x3 = x;
	}
	
	void setY3(int y)
	{
		y3 = y;
	}
	
	void setX4( int x)
	{
		 x4 = x;
	}
	
	void setY4(int y)
	{
		y4 = y;
	}
	
	
	
}
